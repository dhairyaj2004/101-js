//OR operator in js
//The OR (||) operator returns true if either one of the operands is true. If both are false, it will return false.
console.log(0||3);//3
console.log('Dj'||'');//Dj
console.log(undefined||null);//null
//21.js is for AND operator
//It only return falsy value, otherwise it will return the first operand
console.log(0&&3);//0
console.log('Dj'&&123456789);//123456789
console.log(undefined&&null);//undefined